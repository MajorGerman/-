a = [] # Создаем пустой массив
n = int(input("Количество строк: ")) # Задаем размеры
for i in range(n):
    a.append(["."] * n) # Заполняем массив точками
for i in range(n):
    a[i][int(n/2)] = '*'   # Заменяем средний столбик на "*"
for i in range(n):
    a[int(n/2)][i] = '*'    # Заменяем среднюю строку на "*"
for i in range(n):
    for t in range(n):
        if i==t:         # Условие для главной диагонали
            a[i][t] = '*'  # Заменяем  главную диагональ на "*"
for i in range(n):       
    for t in range(n):
        if (n - i) - 1 == t:  # Условие для побочной диагонали
            a[i][t] = '*'  # Заменяем побочную диагональ на "*"
for i in range(n):       # Выводим массив
    print("")
    for t in range(n):
        print(a[i][t], end = ' ')



