#6.Поменять столбцы.
        
def  swap_columns(a, i, j): # Создаем функцию
    for m in range(len(a)):
        for n in range(len(a[m])): # Пробегаем по всем значениям для перемены местами
            if a[m][n] == i:
                a[m][n] = j
            elif a[m][n] == j:
                a[m][n] = i
    return a
